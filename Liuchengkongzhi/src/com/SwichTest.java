package com;

public class SwichTest {

	public static void main(String[] args) {
		int n = 3;
		switch (n) {
		case 1:
			System.out.println(1);
			break;
		case 2:
			System.out.println(2);
			break;
		case 3:
			System.out.println(3);
			break;
		default:
			System.out.println("以上都不是");
			break;
		}
	}

}
